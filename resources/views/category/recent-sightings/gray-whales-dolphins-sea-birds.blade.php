						<h5>Gray, Humpback Whales, Springtime Dolphins</h5>

						<p>Over the last week we have had some delightful whale watching trips! Last Sunday we found lots of gray whales over the outer Monterey Bay, and got great looks at a huge pod of dolphins that swam and played alongside the boat!</p>

						<p>On Tuesday we hosted a group of high school students who brought us great luck – spotting humpback whales near Santa Cruz, and gray whales further out towards Monterey.</p>

						<p>Saturday whale watching another lovely day – between storms we’ve had some beautiful conditions! Guests spotted several humpback whales, and one breached out of the water! We noted that the 2 gray whales we saw on this trip seemed to be heading south! Most likely these animals will be late for the breeding season in Mexico. Usually at this time of year in Monterey we are starting to see gray whales migrating northbound, returning to Alaska for the summer.</p>

						<p>It’s also exciting to see migratory ocean birds visiting Monterey Bay – This trip a keen-eyed passenger spotted a Tufted Puffin!</p>

