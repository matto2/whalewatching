						<h5>Good Day with Gray Whales</h5>

						<p>Sunday 3/12/17 — Good Day with Gray Whales

						<p>The gray whales led the way on Sunday! We kept our whale watching tour along the coastline, following several sets of gray whales swimming northbound through Monterey Bay. There were plenty of gray whales just 1-2 miles offshore from Santa Cruz — wonderful to see whale spouts over here, there, everywhere! We followed several small groups of whales up the coast towards Davenport; a few groups of two, then a trio of gray whales who were the most active, showing tail flukes (diving),  a spyhop, and other active surface behaviors.</p>

						<p>On the way in we got good looks at otters in the kelp beds.  It was a beautiful day on Monterey Bay!</p>

