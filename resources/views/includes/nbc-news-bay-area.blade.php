					<h2>Humpback Whale Hanging Out Near Santa Cruz</h2>

					<p><img src="/images/humpnbc.jpg" alt="Humpback in Santa Cruz while Whale Watching in Monterey Bay California with Stagnaro Charters"></p>

					<p>Perhaps it's a surf bum. Or maybe it's in for a "whale" of a time at the Boardwalk.</p>

					<p>Whatever the cause, an adult humpback whale has made the sleepy beach town of Santa Cruz its stomping grounds -- or at least the shallow waters near Main Beach in Santa Cruz, according to the Santa Cruz Sentinel.</p>

					<p>The whale -- nicknamed "Stinky" by locals, perhaps for its taste for the sardines and anchovies which are prevalent in that part of Monterey Bay -- has been seen in water as shallow as 25 feet, a rarity for such massive mammals, according to whale experts.</p>

					<p>Stinky is 50 feet long which means it is possibly a female, as female humpback whales are bigger than males.</p>

					<p>The whale breaches and generally puts on quite a show for beach-goers, according to Ken Stagnaro of Santa Cruz Whale Watching. But Stinky won't do so for long -- whales move on quickly, so anyone wishing to catch a glimpse should do so soon.</p>

