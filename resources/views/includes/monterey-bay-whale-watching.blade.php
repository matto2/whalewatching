					<h6>Santa Cruz Whale Watching | Whale &amp; Dolphin Cruises</h6>

					<p>Climb aboard the 60 foot Velocity, or 56 foot Legacy, and experience the Monterey Bay area like never before on a Monterey Bay whale watching cruise! In winter or summer, we take you to the heart of whale territory as we spot these magnificent creatures and much more!</p>

					<h2>Summer Whale &amp; Sealife Excursion:</h2>

					<p>May through November: The Humpback Whales frequent the Monterey Bay as their feeding grounds. Climb aboard and join us as we search the bay sanctuary for a variety of marine mammals including: Humpback Whales, Blue Whales, Dolphins, Sea Otters, Sea Lions and more.</p>

					<h2>Winter Whale Watching:</h2>

					<p>December through April: The winter is Gray Whale season. The chance of sighting the Gray Whales at this time is very high, somewhere around 95%!</p>

					<p><img src="/images/velocity.jpg" alt="Velocity Pic"></p>

					<p>20,000 California Gray Whales pass through our bay waters on their annual winter migration from the cold arctic waters, to the warm lagoons of Baja Peninsula. You can get breathtaking close up views of these beautiful cetaceans. Sightings of dolphins and other marine mammals are also common. This is truly an Ocean Adventure for the whole family! All trips are narrated, and offer fascinating insight by our knowledgeable crew. Trips can last approximately 3 to 4 hours, depending on the conditions, as we try to see as much wildlife as possible!</p>

