					<h2>Orcas again this weekend!</h2>					
					<p><em>June 6, 2016</em> Sightings Report for the weekend</p>
					<p><em>Monday:</em> 8 playful friendly Orcas today, out in the fog, plus 6 sleepy Humpback Whales.</p>
					<p><em>Sunday:</em> 10 Orcas — 6 of them swam under the boat! Plus a huge pod of Common Dolphins</p>
					<p><em>Saturday:</em> Humpback whales – lunge feeding</p>
					<p><em>Friday:</em> Humpback whales – breaching!</p>
					<p>All our activity this weekend has been on Santa Cruz side of the bay near the Soquel Hole.</p>
