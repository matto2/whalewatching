					<h1>My Blue Heaven: Abundance of World's largest creature seen in Monterey Bay</h1>

					<p>By Jason Hoppin | Santa Cruz Sentinel</p>
					<p>Posted:   07/08/2013</p>

					<p>SANTA CRUZ — Monterey Bay is singing the blues.</p>

					<p>Majestic blue whales are being spotted around Monterey Bay in recent days, as viewing season for the largest animal in history reaches its peak.</p>

					<p>Whale-watching captains are reporting sightings of more than a dozen of the endangered behemoths, which seem to be venturing into the bay with greater frequency in recent years.</p>

					<p>“(People) are in awe. I hear a lot of gasps, especially when those massive tail flukes come up,” said Dorris Welch, co-owner of Santa Cruz-based Sanctuary Cruises. “Some people just say it’s a life-changing experience.”</p>

					<p>Because they dive deep and long, blue whales can be elusive compared to more visible humpback whales, a playful creature that has provided boaters, kayakers and even surfers with more than a few thrills.</p>

					<p>But the blues are being seen with relative frequency, and are proving a draw for people who pine for a firsthand look.</p>

					<p>“It’s pretty much every trip right now. (Sunday) we saw probably no less than 10,” said Ken Stagnaro of Santa Cruz Whale Watching. “Saturday morning, I followed a whale that just kept taking me into more whales.”</p>

					<p>Due to high spring winds that have increased the nutrient levels in the water, it has been a particularly good season for whale watching. There also have been several reports of pods of Risso’s dolphins, a large, snub-nosed variety that has been seen toying with blue whales.</p>

					<p>“They kind of play with them, kind of harass them,” Stagnaro joked. “On average, it can be kind of a boring lifestyle so they need something to keep them entertained.”</p>

					<p>Giancarlo Thomae, a marine biologist with Sanctuary Cruises, said he believes there’s as many as 15 blues in the bay, with some coming very close to shore near Moss Landing.</p>

					<p>“People are very stoked because they’re the largest animals that ever lived,” Thomae said. “They dwarf the boat “… people get really excited.”</p>

					<p>Beginning about two weeks ago, boaters also have seen harder-to-spot endangered leatherback sea turtles. Now the state’s official marine reptile, the giant turtles travel from Indonesia to feed on the region’s abundance of jellyfish.</p>

					<p>Leatherbacks typically stay until August, when they venture back across the Pacific Ocean.</p>

