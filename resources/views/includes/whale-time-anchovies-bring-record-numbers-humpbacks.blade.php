					<h1>A Whale of a time: Anchovies bring record numbers of Humpbacks</h1>

					<p>By Jason Hoppin | Santa Cruz Sentinel</p>
					<p>POSTED:   09/13/2013 04:42:59 PM PDT</p>

					<p>SANTA CRUZ — Unprecedented numbers of whales have invaded Monterey Bay, on the hunt for epic schools of anchovies and delighting nature lovers and sightseers.</p>

					<p>Local whale-watch pilots say pods of whales are joining herds of sea lions and flocks of birds to dine on the tiny green fish. Estimates range into the hundreds for humpback whales, though blue whales have been spotted too.</p>

					<p>“It’s the most whales that I’ve seen since I’ve been doing this, over 26 years,” said marine biologist Nancy Black, of Monterey Bay Whale Watch. “There are so many humpbacks in the bay.”</p>

					<p>Some say they numbers have spiked in recent days, while others say they’ve been here for several weeks. Humpback sightings usually peak later in the year, but volumes of whales are being reported near Moss Landing and Santa Cruz.</p>

					<p>The marine creatures are drawn by massive schools of anchovies, with Black spotting one school that she estimated to be 200-feet deep and more than a mile long.</p>

					<p>“You can see it on the depth finder,” Black said. “It’s really amazing. I don’t know how long it’s going to last.”</p>

					<p>Ken Stagnaro of Santa Cruz Whale Watch said he’s recently captained trips where dozens of whales were spotted not far from Santa Cruz.</p>

					<p>“There’s a bay full of whales out there,” Stagnaro said. “(Tuesday’s) trip we probably saw 30 to 40 humpback whales within two to three miles of Santa Cruz.”</p>

					<p>Whales usually come to Monterey Bay for krill. Stagnaro said anchovies are cyclical, typically showing up in late summer when local stream flows are low and don’t dilute the salinity of near-shore saltwater. That creates conditions the anchovies like, he said.</p>

					<p>Giancarlo Thomae, a marine biologist with Moss Landing’s Sanctuary Cruises, said he ventured onto the bay with a couple friends on kayaks Wednesday, and saw numerous whales, Risso’s dolphins and even an elephant seal.</p>

					<p>“It’s just phenomenal,” Thomae said.</p>

					<p>Whales appear to be congregating near the edges of the underwater Soquel Canyon. Even though Labor Day signals the unofficial end of the local tourist season, tour guides say crowds have been pretty strong.</p>

					<p>They also wanted to remind people on the water that there are strict rules against approaching marine mammals too closely. In addition, Thomae said only experienced kayakers should go whale-watching on personal watercraft, noting that tides can shift suddenly over deep waters.</p>

