					<h1>Year of the Whale: 2013 brought a marine show unlike any other</h1>

					<p class="mb0">By Jason Hoppin | Santa Cruz Sentinel</p>
					<p>POSTED:   12/28/2013</p>

					<p>MOSS LANDING.– Just when you think nature couldn’t impress you more, something like 2013 happens.</p>

					<p>This was year the Monterey Bay turned the dial up to 11, producing a months-long carnival of lunging humpback whales, bird clouds, dolphin wizardry, frenzied sea lions, playful killer whales and even visits from marine royalty — blue whales.</p>

					<p>“Best year I’ve ever seen and best year anybody’s seen that I’ve talked to,” said Giancarlo Thomae, a 25-year-old UC Santa Cruz graduate whose near-daily forays onto the bay, by boat or kayak, helped document Mother Nature’s best production in a long time.</p>

					<p>The wildlife display on Monterey Bay was selected by Sentinel staff as a top news-maker in 2013.</p>

					<p>The true stars were hundreds of humpback whales that lingered for months in the northeast part of the bay, with some here even still. People crammed tour boats, hopped on paddleboards and leaned out over wharf railings for a glimpse, and were almost universally rewarded with one.</p>

					<p>The spectacle was due to the presence of huge numbers of anchovies, epic schools that boat captains said would go on for miles. Their numbers were such that a school even rushed in to the Santa Cruz Small Craft Harbor, leading to an October die-off that fouled the air for weeks.</p>

					<p>Why the anchovies? People cited a healthier ocean, a lack of sardines or predators such as salmon and a cool-water cycle coupled with normal upwelling of nutrients from the underwater Monterey Canyon.</p>

					<p>“The canyon is just basically a big giant doorway to the open ocean, and it’s right here on our doorstep. That’s what makes it so special,” said Ken Stagnaro of Santa Cruz Whale Watching. “We’re pretty lucky to be here. You get these animals up and down the coast, but not really condensed like this.”</p>

					<p>At the Monterey Bay Aquarium, first-hand looks at marine life began to rival the interpretive exhibits, the open sea becoming an attraction alongside jellyfish and kelp forests. Guests flocked to an outdoor pavilion for a look at birds, sea otters and especially abundant whales.</p>

					<p>“We have a new interpretive station at the Aquarium. We call it ‘Today on the Bay.’ A lot of us said one of the best exhibits at the Monterey Bay Aquarium is the Monterey Bay itself,” said Jim Covel, director of guest experience training and interpretation. “We’ve got a front-row seat here.”</p>

					<p>Photographers such as Thomae and Bonny Doon’s Jodi Frediani procured amazing shots of the activity. Through the Internet and social media, word spread and the photos became apertures into life on the water, a rabbit hole through which the whole world seemed to want to jump.</p>

					<p>“Let’s put it this way: before all this happened, there were times when we barely had enough people to go out. It really blew up,” said Thomae, who worked for Sanctuary Cruises Whale Watching this summer. “We had people calling us from Hong Kong, Sydney, all over the world, saying, ‘Oh the whales are thick? I want to go.'”</p>

					<p>One Thomae photo showing a local kayak guide, Karen Hatch, framed by the body of a whale went viral. It was featured everywhere from CBS’ “This Morning” to the German news magazine Der Speigel.</p>

					<p>Despite widespread TV coverage, tour captains say the biggest impact came when the most traditional of all media, The New York Times, weighed in. The newspaper produced a popular multimedia story on Monterey Bay.</p>

					<p>“You know what? Hands down, nothing got the response like when the New York Times article came out,” Stagnaro said. “The phones just went crazy.”</p>

					<p>Once near extinction, humpbacks are a species on the rebound. That is one reason Stagnaro thinks they’re here to stay year-round, as long as there’s enough food.</p>

					<p>“I don’t expect it to change anytime soon,” he said.</p>

					<p>Even around Christmas, the show continued. Normal patterns of migratory grey whales are coming through, but several humpbacks remain in the bay, along with a handful of killer whales.</p>

					<p>“Weather permitting, you could see whales for pretty much 365 days in the Monterey Bay during the year,” Thomae said.</p>

