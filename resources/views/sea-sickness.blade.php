					<h1>Ocean Conditions &amp; Sea Sickness Prevention</h1>

					<p>The Monterey Bay National Marine Sanctuary is full of wildlife and natural marine treasures that can be viewed any time of year. Cloudy or sunny, it is worth exploring the Bay all year-round and in all types of weather and ocean conditions. A bit of rain or swell does not necessarily cancel a trip, so here are a few tips to be prepared for your whale watching adventure.</p>

					<p>In general, it is best to dress warmly in layers, bring sunscreen, a hat, and wear closed-toe rubber soled shoes. Of course there is the dreaded subject of seasickness. We certainly hope no one feels seasick, but it happens sometimes. Both of our boats offer a smooth ride overall, but whale watching tours sometimes go far out into the Bay and yes, a small percentage of passengers are known to become seasick in a variety of ocean conditions.  If you are sensitive to motion sickness, proper precautions to avoid it are highly recommended.</p>

					<ul>
						<li>Get a good night's sleep – it helps to be well-rested before going on your whale watching trip.</li>
						<li>Stay hydrated by drinking plenty of water and avoiding coffee.</li>
						<li>Eat breakfast – Do not sail on empty stomach. A non-greasy meal such as oatmeal, toast, or a little yogurt is fine.</li>
						<li>Try an over-the-counter medication such as Bonine or Dramamine. It is recommended to take at least 1-2 hours before you get on the boat.</li>
						<li>Try ginger – Ginger tea, ginger snaps, sushi ginger – this is an ancient natural remedy for tummy upset. Ginger Chews and bonine tablets are available at our ticket office.</li>
						<li>Acupressure wrist bands are a popular way to suppress the nerves that transmit feelings of nausea. We recommend <a href="http://www.psibands.com/" title="Psi Bands" target="_blank">Psi Bands</a> <em>which are available at our ticket office during check-in.</em></li>
						<li>Avoid alcohol the night before. This is one of the most common reasons people are sick on the boat.</li>
						<li>Stay out in the fresh air. If you are feeling nauseous, avoid heading towards the bathroom. Instead, head to the back of the boat.</li>
						<li>Keep your focus on the horizon, towards the forward bow of the boat will help you keep your balance and get your sea legs.</li>
						<li>Eat crackers, drink ginger ale or lightly carbonated water to settle your stomach.</li>
						<li>While it is no fun, if you do get sick, simply lean over the water and let it rip. No one will laugh – and if they do, position yourself a little upwind and they won’t laugh long.</li>
					</ul>

