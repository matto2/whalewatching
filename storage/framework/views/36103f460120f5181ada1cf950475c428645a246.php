					<h2>Sunday 11/6/16 ~ Whale Watching on ‘Legacy’</h2>
					<p>Five humpbacks, one <a href="http://wildwhales.org/minke-whale/" title="minke whale">minke whale</a>, and 200 to 300 common dolphins west of the Soquel Canyon.</p>
					<p>Friday, 11/4/16 ~ Whale Watching on ‘Legacy’</p>
					<p>Group of four humpbacks over the Soquel Canyon. Pair of humpbacks one of which did a couple of tail throws, and a few scattered whales throughout the Monterey Bay. A couple of large packs of sea lions swimming with the whales.</p>
					<p>We also continue to see a few whales near West Cliff Drive in Santa Cruz.</p>
