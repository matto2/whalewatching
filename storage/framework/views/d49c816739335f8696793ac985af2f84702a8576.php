					<h2>Albatross, Gray and Humpback Whales</h2>
					<p><img src="/images/120.jpg" alt="Albatross, Gray and Humpback Whales while Whale Watching in Monterey Bay California with Stagnaro Charters"></p>
					<p>Saturday, April 2: Today the fog burned off early and we set off on board Velocity to see what wildlife we could find in Monterey Bay!</p>
					<p>We saw:</p>
					<ul>
						<li>Four gray whales – 2 pairs — near Santa Cruz</li>
						<li>One Black-footed <a href="https://www.allaboutbirds.org/guide/Black-footed_Albatross/lifehistory/" title="Albatros" target="_blank">albatross</a> (archive photo John Bruckman)</li>
						<li>One humpback whale that was lunge feeding, showing lots of surface behavior: horizontal, vertical lunges and rolling around near to the boat.</li>
						<li>One tiny sunfish (mola-mola), and many harbor porpoises and otters.</li>
					</ul>
