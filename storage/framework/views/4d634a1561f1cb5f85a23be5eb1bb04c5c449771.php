					<h1>Whale watchers get up close, personal in Santa Cruz Harbor</h1>

					<p><img src="/images/upclose.jpg" alt="Whale while Whale Watching in Monterey Bay California with Stagnaro Charters"></p>

					<h3>Santa Cruz Harbor 10/27/11</h3>

					<p>SANTA CRUZ - The past couple of weeks have been busy for whale watchers in Santa Cruz.</p>

					<p>At sunset on Wednesday, boaters hung out with six to eight humpback whales cruising around the harbor. Last Friday, school kids got a show from two humpbacks that breached and rolled only 30 feet from their catamaran.</p>

					<p>The humpbacks are feeding on anchovies and other baitfish, explained Laura Barnes, education coordinator with O'Neill Sea Odyssey, a marine science program for fourth-graders through sixth-graders.</p>

					<p>"There is a huge amount of plankton in the water and the baitfish are here because of that," she said.</p>

					<p>A handful of sailboats, a small catamaran and an adventurous stand-up paddle boarder watched more humpback activity Wednesday evening, just a mile from shore.</p>

					<p>Ken Stagnaro, owner of Santa Cruz Whale Watching, took his family out for a sunset excursion.</p>

					<p>"Monterey Bay is one of the top three places in the world to see marine wildlife this close to shore," Stagnaro said. He followed several whales for an hour before the sun slipped below the horizon.</p>

					<p>One of the whales was Stinky, a local celebrity that has been hanging around the bay all summer. Stagnaro recognized Stinky by a white streak on the whale's back.</p>

					<p>Barnes described last Friday's outing with a group of school kids as amazing. Dolphins approached the boat, and then headed towards a group of seagulls and pelicans diving on fish near the surface.</p>

					<p>"Then we saw two whales come up at the same time," Barnes said. "The kids were blown away. We had to sort of give up teaching for a moment."</p>

					<p>The class was able to keep track of the whales even underwater because the cetaceans made a "bubble net." The whales blow bubbles in a circle and herd fish into a ball so it is easy to grab a big mouthful.</p>

					<p>"We saw it coming up with its mouth open," said Barnes. "The gullet completely filled up and fish and water spilled out of its mouth."</p>

					<p>The whale also slapped the water a few times with its long pectoral fins and leaped, or breached, out of the water.</p>

					<p>Humpback whales are known for their showy behavior. Stagnaro takes whale watchers, out on his yacht four to six times a week.</p>

					<p>"Locals come for the gray whales," he said. "But you'll never get a show from a gray whale the way you will with a humpback."</p>

					<p>Gray whales often come close to shore during their migration, swimming through Santa Cruz waters in mid-January headed south and in mid-March headed north.</p>

					<p>"Everyone calls it whale season, but that's just one species that are passing through," Stagnaro said. "Whales are here year-round."</p>

					<p>Santa Cruz Whale Watching, operated by Stagnaro Charter Boats, takes people out on the bay all year. Cruises might encounter harbor seals, sea otters, as many as five species of dolphins and four species of whales, including blue whales, killer whales, gray whales and humpbacks.</p>

					<p>Humpback whales are typically about 50 feet and weigh around 45 tons.</p>

					<p>"Ideally this time of the year they will be quite a bit heavier than in the spring," Stagnaro said. He explained that baleen whales, like humpbacks, feed all summer in colder waters to build up weight and blubber. They head south in the winter for calving in warmer waters.</p>

					<p>Humpback whales are found in all the oceans of the world. The humpbacks in the bay now are part of a group of several hundred who travel the coast between Oregon and Mexico.</p>

					<p>Stagnaro does caution boaters to be aware of the guidelines for whale watching. "Stay a little behind and to the side of them. As long as you're not cutting them off and making them alter course."</p>

					<p>The Marine Life Protection Act requires that whale watchers stay at least 100 yards away, though the whale may approach the boat on its own.</p>

					<p>"They are curious animals," Stagnaro said. "Sometimes they will come up and you'll be eye to eye with a whale."</p>

