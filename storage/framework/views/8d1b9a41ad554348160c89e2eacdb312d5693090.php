					<h1>Monterey Bay Whale Watching: Orcas, humpbacks, blue whales spotted in Monterey Bay</h1>

					<p>By ROMAIN FONSEGRIVES — Santa Cruz Sentinel</p>

					<p>Posted:   06/18/2012 06:22:47 PM PDT</p>

					<p>SANTA CRUZ – Monterey Bay whale-watching tours usually provide onlookers with unforgettable sights of majestic, playful humpback whales. But in recent days, cruisers had a stroke of luck, encountering orcas and a blue whale – two much rarer species to see.</p>

					<p>These encounters did not draw as much attention as last fall, when humpback whales paraded just a few hundred yards from Santa Cruz shores, shedding international light on Monterey Bay’s wildlife. But glimpses of orcas and blue whales are always an amazement, said Kenny Stagnaro of Stagnaro Sport Fishing, Charters and Whale Watching Cruises.</p>

					<p>“It’s not uncommon at all to see them, but it’s definitely not an every-day occurrence,” Stagnaro said of the orcas.</p>

					<p>To witness a blue whale breaching the water is a precious sight, Stagnaro said. An endangered species, blue whales comprise a world population that ranges from 5,000 to 12,000, according to a 2002 report of the Committee on the Status of Endangered Wildlife from Canada.</p>

					<p>A group was cruising just a few miles south of Santa Cruz Small Craft Harbor on Saturday, Stagnaro said, when those aboard suddenly spotted male and a female orcas coming to the surface. Transient killer whales such as these two are regularly observed in the Monterey Bay, navigating in pods and preying on other mammals including seals, sea lions, dolphins or other whales’ calves.</p>

					<p>“They’ll chase pretty much every mammal smaller than they are,” said Giancarlo Thomae, a marine biology senior student at UC Santa Cruz, who works as a naturalist for cruising companies in the Monterey Bay.</p>

					<p>“I’m sure these two weren’t by themselves,” Stagnaro added. “Their family were probably a few miles off, but we didn’t get to see them.”</p>

					<p>Cruising ship Velocity tagged along with the predators for 40 minutes, Stagnaro said. The killer whales’ dorsals were leaping out of the water every once in a while above the Soquel Canyon. Yet, whale-watchers witnessed no kill Saturday.</p>

					<p>“They were on patrol,” Stagnaro said. “The Monterey Canyon is a pretty well-known hunting zone for killer whales. It drops from 300 feet to 3,000 feet deep. Experts say the canyon is a kind of echo chamber that makes their echo location more effective.”</p>

					<p>While being interviewed by phone Monday, Stagnaro was tailing a 90-feet blue-whale, he said.</p>

					<p>“This thing is huge, we are feeling really small right now,” Stagnaro said. “This is really close to the shoreline to see a blue whale, but there’s a lot of krill in this area.”</p>

					<p>A charter boat operator for 28 years in Santa Cruz, Stagnaro said he had not seen so much krill in the Monterey Bay for years. With the crunchy crustacean blooming all over the Monterey</p>

					<p>“It popped up right next to the boat with its mouth wide open before rolling over on its belly,” Stagnaro said. “The water just turned plain red because of the krill’s color. It’s been an amazing show.”</p>

					<p>“That blue whale was absolutely massive,” said Debbie Ojeda, one of Stagnaro’s customers, after coming back ashore Monday. “I know people who’ve been on whale-watching trips and who’ve never seen anything. So I definitely feel lucky.”</p>

					<p>Whale watching season is approaching its climax, Thomae said.</p>

					<p>“We happened to see more whales since the first part of May,” Thomae said. “And now we’re approaching krill’s peak production. Anytime during next month will be the best to go see these animals.”</p>

					<p>Stagnaro’s cruises also caught a glimpse of two fin whales and around 300 humpback whales in the past couple of weeks. But orcas and blue whales, although regular visitors of the bay, do not come across the crew more than 30 or 40 times a year, the captain said.</p>

					<p>“It exceeds most people’s expectations,” said Stagnaro, thrilled. “They don’t believe their own eyes.”</p>

