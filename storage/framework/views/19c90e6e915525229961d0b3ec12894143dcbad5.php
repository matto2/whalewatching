					<h3>Friendly humpback whales this weekend</h3>
					<img src="/images/186.jpg" alt="Humpbacks while Whale Watching in Monterey Bay California with Stagnaro Charters">
					<p>Saturday, May 16, 2105: We counted 12 humpback whales out in the center of Monterey Bay. Four very friendly humpback whales stayed with the boat for a half-hour! These friendly whales were spy-hopping, pec-slapping, breaching and spinning near the boat, and looking up "people watching." Everyone got sprayed by their spout!</p>
					<img src="/images/187.jpg" alt="Whale while Whale Watching in Monterey Bay California with Stagnaro Charters">
					<p class="cr">photo: Michael Nelson</p>
					<p>Sunday, May 17, 2015: There were 15 humpback whales on Sunday, and what a show! We got to see all types of whale surface behavior on display, including tandem breaches! Tandem tail lobbing! One whale approached the boat to look up at the people, another friendly encounter.</p>
