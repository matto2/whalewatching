					<p>The Short-beaked Common Dolphin (Delphinus delphis) is a species of Common Dolphin. It has a larger range than the Long-beaked Common Dolphin (D. capensis), occurring throughout warm-temperate and tropical oceans, with the possible exception of the Indian Ocean. There are more Short-beaked Common Dolphins than any other dolphin species in the warm-temperate portions of the Atlantic and Pacific Oceans. The color pattern on the body is unusual. The back is dark and the belly is white, while on each side is an hourglass pattern colored light grey, yellow or gold in front and dirty grey in back.</p>

					<h4>BOTTLENOSE DOLPHIN: FAIRLY COMMON</h4>

					<p> The one most people are familiar with “flipper” generally a warmer water species are more common in the bay since the El Niño in the early 1980’s. Bottlenose seem to be more common in the northern or Santa Cruz side of the bay. These frequent bowriders can sometimes be seen on our inner bay sunset cruise just outside the surf chasing feed.</p>

					<h4>NORTHERN RIGHT WHALE DOLPHINS: COMMON</h4>

					<p class="mb0"><img src="/images/bottlenose.jpg" alt="Bottlenose Dolphin while Whale Watching in Monterey Bay California with Stagnaro Charters"></p>

					<p class="i">Northern Right Whale Dolphin Lissodelphis borealis</p>

					<p>Found in the cooler North Pacific, these Dolphins are unique in that they have no dorsal Fin. They are often found mixed with other species of dolphin. They are very dark in color.</p>

					<h4>DALL’S PORPOISE: COMMON</h4>

					<p class="mb0"><img src="/images/dalls.jpg" alt="Dalls Porpoise while Whale Watching in Monterey Bay California with Stagnaro Charters"></p>

					<p class="i">Dall’s Porpoise Phocoenoides dalli</p>

					<p>Dall’s Porpoise are painted black and white. Visitors on their first trip often mistakenly call them baby Killer Whales. They will often zigzag around at great speed on or just below the surface of the water creating a spray called a “rooster tail”. They may appear and disappear quite suddenly. The fastest of all small cetaceans, Dall’s Porpoises can swim at up to 55 km/h, almost as fast as the Orca. The porpoises will approach boats and will bow- and stern-ride, but may lose interest, unless the boat is traveling quickly.</p>

					<p>Dall’s Porpoises appear in small groups (about 2-10 in number). The groups appear not to be tight knit and may aggregate together at good feeding grounds. On very rare occasions a thousand or more individual may be seen at the same time. They feed on a variety of fish and cephalopods. Shoals of fish such as herring, anchovies and mackerel are common meals.</p>

					<h4>HARBOR PORPOISE: COMMON</h4>

					<p>Harbor Porpoise are found year round in shallow coastal areas and are very shy around boating traffic. Rarely does a person get more than one or two glimpses.</p>

