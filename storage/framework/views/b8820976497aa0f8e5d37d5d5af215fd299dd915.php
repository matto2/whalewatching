					<h3>RISSO’S DOLPHIN: COMMON</h3>

					<p class="mb0"><img src="/images/rissos.jpg" alt="Risso's Dolphin while Whale Watching in Monterey Bay California with Stagnaro Charters"></p>

					<p class="i">Risso's Dolphin Grampus griseus</p>

					<p>This larger variety of Dolphin can usually be easily identified by their large dorsal fin and blunt shaped head. These deep divers feed mostly on squid.</p>

					<p>The species’ common name is from Mr. Risso who described a specimen to Cuvier on which Cuvier made his first description. Another common name for Risso’s Dolphin is the Grampus (also the species genus), although as a common name this was historically used for the Orca It is found worldwide in temperate and tropical waters, usually in deep waters rather than close to land. As well as the tropical parts of the Indian, Pacific and Atlantic Oceans, The population around the continental shelf of the United States has been recorded to be in excess of 60,000. In the Pacific a census recorded 175,000 individuals in eastern tropical waters and 85,000 in the west. No global estimate of population exists.</p>

					<h2>COMMON DOLPHIN: FAIRLY COMMON</h2>

					<p class="mb0"><img src="/images/common.jpg" alt="Short Beaked Common Dolphin while Whale Watching in Monterey Bay California with Stagnaro Charters"></p>

					<p class="i">Short-beaked Common Dolphin Delphinus delphis</p>

					<p>Common Dolphins can be observed in very large groups and are generally found in warmer waters. They are found worldwide.</p>

