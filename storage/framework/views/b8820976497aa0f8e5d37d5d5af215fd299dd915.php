					<h1>Dolphins &amp; Porpoises</h1>

					<p class="mb0"><img src="/images/pacific.jpg" alt="Pacific White-sided Dolphin while Whale Watching in Monterey Bay California with Stagnaro Charters"></p>

					<p class="i">Pacific White-sided Dolphin Lagenorhynchus obliquidens</p>

					<p>These acrobats of the sea can be found in groups of over a thousand and are often the highlight of our Monterey whale watching trips. These generally smaller Dolphins (7′) often exhibit behaviors that still dazzle even the boat captains and crew. With high aerial jumps and flips they will give you all that you and your camera can capture.</p>

					<p>The Pacific White-sided Dolphin has three tones of color. The chin, throat and belly are creamy white. The beak, flippers, back, and dorsal fin are a dark gray. There are light gray patches on the sides and a further light gray stripe running from above the eye to below the dorsal fin where it thickens along the tail stock. A dark gray ring surrounds the eyes. The Pacific White-sided Dolphin is extremely active and mixes with many of the other cetacean species to be found in the north Pacific, Monterey Bay. It also readily approaches boats and bow-rides. Large groups are common; on average 90 individuals per group but super groups of more than 3,000 have been recorded. Prey is mainly hake, anchovies, squid, herring, salmon and cod.</p>

