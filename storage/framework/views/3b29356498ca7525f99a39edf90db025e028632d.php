					<p>Come visit us at the Migration Festival at Natural Bridges State Park and enter to win a Free Whale Watching Trip!</p>
					<p>*Saturday, February 11 from 11 AM to 4 PM</p>
					<p>*End of West Cliff Dr. at Swanton Blvd., Santa Cruz, California 95060</p>
					<p>Come to Natural Bridges State Beach for a full day of activities that celebrate the migration of whales, butterflies, birds and the many creatures that travel. The park will host migratory animal talks, active kids’ games, crafts, skits, live music by the nature-loving 5M’s Band, educational booths and displays, along with the now-famous free habitat-cake served at the end of the event. You can purchase a picnic at the park or bring your own. Event is free; vehicle day-use fee is $10. For more information, please call (831) 423-4609 or email NaturalBridgesStateBeach@gmail.com.</p>
