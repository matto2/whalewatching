					<h1>The show goes on: Whales, birds, dolphins still drawing crowds</h1>

					<h2>Whales, birds, dolphins still drawing crowds</h2>
					<p>By Jason Hoppin | jhoppin@santacruzsentinel.com @scnewsdude  on Twitter</p>
					<p>Posted:   08/14/2014 04:01:16 PM PDT2 Comments</p>

					<p>MOSS LANDING >> The music may have stopped, but the show never really ends.</p>

					<p>Humpback whales aren’t getting the same worldwide attention as a few weeks ago, when they practically seemed to be knocking on the doors of beachfront homes. Lunchtime gawkers at the Santa Cruz Municipal Wharf got an eyeful, as did visitors to Moss Landing State Beach, where the whales came close enough to shore that they seemed eye-to-eye with beachgoers, including a few confused dogs and horses.</p>

					<p>But they’re still out there, in abundance, entertaining visitors from across the globe.</p>

					<p>“It was amazing because at one moment you don’t know where to see, there’s a lot to look at,” said Laetitia David, who lives outside Versailles, France, and is touring the western U.S. with her husband, Alain.</p>

					<p>The couple took a tour this week with Moss Landing-based Sanctuary Cruises, one of several whale-watching boats operating out of the region that have been teeming with passengers.</p>

					<p>The giant schools of anchovies that seem to yawn from shore out to the horizon have dissipated, a phenomenon that drew more whales but that seems to have been punctuated by the unfortunately stinky harbor die-off in Santa Cruz.</p>

					<p>There are always more visitors to Monterey Bay, and last weekend Monterey Bay Whale Watch’s posted pictures of killer whales leaping out of the water in its Facebook page. Of course, birds, seals and dolphins can always be found in abundance.</p>

					<p>You might even see another rarity of you’re near the water these days – surfers without wetsuits. Water temperatures at the National Oceanic and Atmospheric Administration’s Monterey Bay buoy are in the mid-60s this week, well above normal even for late summer.</p>

					<p>And that buoy is 30 miles offshore. Near-shore temps are certain to be higher, all the result of calm skies that have ground to a halt the phenomenon known as upwelling, where winds churn ocean waters, bringing cooler, nutrient-rich deep sea water to the surface.</p>

					<p>“It’s just a lack of northwest wind, which gives us upwelling along the coast,” National Weather Service forecaster Steve Anderson said. “The cold water isn’t coming to the surface, which allows the water temperature to warm up.”</p>

					<p>The warm water has nothing to do with El Niño, Anderson stressed.</p>

					<p>Santa Cruz Whale Watching’s Ken Stagnaro said the lack of upwelling have kept the krill counts low, which could be why there haven’t been reports of blue whales so far.</p>

					<p>But Stagnaro said there’s still plenty to see on the water. “We had sightings of killer whales feeding on a sea lion a few days ago,” Stagnaro said.</p>

					<p>The show must go on, after all.</p>

