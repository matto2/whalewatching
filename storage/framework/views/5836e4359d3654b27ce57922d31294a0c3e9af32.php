					<h1>Humpback whales return to Monterey Bay</h1>
					<p><img src="/images/hump.jpg" alt="Humpback while Whale Watching in Monterey Bay California with Stagnaro Charters"></p>					
					<p>Humpback whales return to Monterey Bay</p>
					<p>By Stephen Baxter – Santa Cruz Sentinel | Posted: 04/26/2012</p>
					<p>MOSS LANDING – Majestic humpback whales have returned to Monterey Bay with some close encounters with whale-watching groups this week.</p>
					<p>Michael Sack of Sanctuary Cruises took a group of high-schoolers out Thursday on his 43-foot boat. About 10 miles southwest of Moss Landing, they spotted a humpback mother and calf.</p>
					<p>He said they spy-hopped, or raised their head above the water as they edged toward the boat.</p>
					<p>“They’re interested in us and they’ll look at us,” Sack said.</p>
					<p>“It’s a very moving experience. It’s amazing,” Sack said. “It’s pretty rare that we have them hang out with us for two hours.”</p>

					<p>The humpback whales usually spend the winter off Latin America. They arrived in Monterey Bay earlier this month and tend to stay to November or December.</p>

					<p>They come to feed on krill – similar to small shrimp – in the underwater canyon off Moss Landing. In the coming months the whales often come closer to shore to eat anchovies and other bait fish.</p>

					<p><em>Ken Stagnaro</em> of <a href="http://stagnaros.com" title="">Santa Cruz-based Stagnaro Sport Fishing</a>, Charters and Whale Watching Cruises said he was on a fishing trip off Davenport on Thursday when he saw humpback whales breach, or leap out of the water, several times.</p>

					<p>“They were making an incredible splash down. It looked like a bomb going off,” said Stagnaro.</p>

					<p>He said April and May can be good months to see a variety of aquatic animals. The humpbacks arrive and gray whales migrate through the area. Orcas, or killer whales, also feed on the gray whale calves.</p>

					<p>“It’s not a pretty thought, but it’s nature at work,” Stagnaro said.</p>

					<p>Stagnaro saw humpbacks on Sunday about 9 miles south of Santa Cruz…</p>

					<p>…In Santa Cruz County, Monterey Bay Whale Watching shows this spring follows a banner fall.</p>

					<p>Humpbacks lazed off the Santa Cruz shoreline for weeks – attracting international attention.</p>

					<p>Stagnaro said he had seen whales come that close to shore off Manresa State Beach in the past, but it was a treat for them to be closer to Santa Cruz last fall.</p>

					<p>They were easily seen from kayaks, surfboards and boats. Authorities warned onlookers to stay 100 yards away – for the safety of whales and humans.</p>

